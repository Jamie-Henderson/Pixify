let counter = 0;

function prevNext (direction) {

    if (counter < currentCard.images.length -1 && direction === "next"){
        counter++;
    }
    else if(counter > 0 && direction === "previous"){
        counter--;
    }

    document.querySelector('.currentPic').src = currentCard.images[counter];
    document.querySelector('.imageCount').innerText = `${counter + 1} of ${currentCard.images.length}`;

};

document.getElementById('prevButton').addEventListener('click', ()=>prevNext('previous'));
document.getElementById('nextButton').addEventListener('click', ()=>prevNext('next'));
  